// Fun√ß√£o principal de limpeza
async function optimizeCache() {
  logStep('START', 'Iniciando otimiza√ß√£o de cache...', 'magenta');
  
  let totalSizeRemoved = 0;
  let filesRemoved = 0;
  
  // Calcular tamanho total antes da limpeza
  const sizeBefore = cleanupPaths.reduce((total, item) => {
    const size = getDirSize(item.path);
    if (size > 0) {
      log(`üìÅ ${item.description}: ${formatBytes(size)}`, 'cyan');
    }
    return total + size;
  }, 0);
  
  if (sizeBefore > 0) {
    log(`\nüìä Tamanho total do cache: ${formatBytes(sizeBefore)}`, 'yellow');
  }
  
  // Remover arquivos e diret√≥rios
  logStep('CLEAN', 'Removendo arquivos de cache...', 'blue');
  
  for (const item of cleanupPaths) {
    if (item.type === 'dir') {
      if (dirExists(item.path)) {
        const size = getDirSize(item.path);
        if (removeDirectory(item.path)) {
          totalSizeRemoved += size;
          filesRemoved++;
          log(`üóëÔ∏è  ${item.description} removido (${formatBytes(size)})`, 'green');
        }
      }
    } else if (item.type === 'file') {
      if (fileExists(item.path)) {
        const size = fs.statSync(item.path).size;
        if (removeFile(item.path)) {
          totalSizeRemoved += size;
          filesRemoved++;
          log(`üóëÔ∏è  ${item.description} removido (${formatBytes(size)})`, 'green');
        }
      }
    }
  }
  
  // Verificar integridade do package.json
  logStep('CHECK', 'Verificando integridade do package.json...', 'blue');
  try {
    const packageJson = JSON.parse(fs.readFileSync('package.json', 'utf8'));
    logStep('‚úì', 'package.json est√° v√°lido', 'green');
  } catch (error) {
    logStep('‚úó', 'package.json est√° corrompido!', 'red');
    throw new Error('package.json corrompido. Restaure de um backup.');
  }
  
  // Verificar integridade do next.config.js
  logStep('CHECK', 'Verificando integridade do next.config...', 'blue');
  const nextConfigFiles = ['next.config.js', 'next.config.mjs', 'next.config.ts'];
  let nextConfigExists = false;
  
  for (const configFile of nextConfigFiles) {
    if (fileExists(configFile)) {
      nextConfigExists = true;
      logStep('‚úì', `${configFile} encontrado`, 'green');
      break;
    }
  }
  
  if (!nextConfigExists) {
    logStep('‚ö†Ô∏è', 'Nenhum next.config encontrado', 'yellow');
  }
  
  // Limpar cache do sistema operacional (opcional)
  logStep('SYSTEM', 'Limpando cache do sistema...', 'blue');
  
  if (process.platform === 'win32') {
    // Windows
    runCommand('npm cache clean --force', 'Limpando cache do NPM', true);
    runCommand('yarn cache clean', 'Limpando cache do Yarn', true);
    runCommand('pnpm store prune', 'Limpando store do PNPM', true);
  } else {
    // Unix/Linux/macOS
    runCommand('npm cache clean --force', 'Limpando cache do NPM', true);
    runCommand('yarn cache clean', 'Limpando cache do Yarn', true);
    runCommand('pnpm store prune', 'Limpando store do PNPM', true);
  }
  
  // Verificar se o node_modules precisa ser reinstalado
  logStep('DEPS', 'Verificando depend√™ncias...', 'blue');
  
  if (!dirExists('node_modules') || !fileExists('node_modules/.package-lock.json')) {
    logStep('‚ö†Ô∏è', 'node_modules pode precisar ser reinstalado', 'yellow');
    log('üí° Execute: npm run fresh:install', 'cyan');
  } else {
    logStep('‚úì', 'node_modules parece estar √≠ntegro', 'green');
  }
  
  // Relat√≥rio final
  logStep('REPORT', 'Relat√≥rio de otimiza√ß√£o', 'magenta');
  log(`üìä Espa√ßo liberado: ${formatBytes(totalSizeRemoved)}`, 'green');
  log(`üóÇÔ∏è  Itens removidos: ${filesRemoved}`, 'green');
  
  if (totalSizeRemoved > 0) {
    log(`‚úÖ Cache otimizado com sucesso!`, 'green');
  } else {
    log(`‚ÑπÔ∏è  Cache j√° estava limpo`, 'blue');
  }
  
  // Recomenda√ß√µes
  logStep('TIPS', 'Recomenda√ß√µes:', 'cyan');
  log('‚Ä¢ Execute este script regularmente para manter a performance', 'cyan');
  log('‚Ä¢ Use "npm run dev:clean" para desenvolvimento limpo', 'cyan');
  log('‚Ä¢ Use "npm run build:clean" para builds limpos', 'cyan');
  log('‚Ä¢ Use "npm run recovery" em caso de problemas graves', 'cyan');
  
  logStep('DONE', 'Otimiza√ß√£o conclu√≠da!', 'green');
}

