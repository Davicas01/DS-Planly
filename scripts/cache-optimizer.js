// Fun√ß√£o para verificar se um diret√≥rio existe
function dirExists(dirPath) {
  try {
    return fs.existsSync(dirPath) && fs.statSync(dirPath).isDirectory();
  } catch (error) {
    return false;
  }
}

// Fun√ß√£o para verificar se um arquivo existe
function fileExists(filePath) {
  try {
    return fs.existsSync(filePath) && fs.statSync(filePath).isFile();
  } catch (error) {
    return false;
  }
}

// Fun√ß√£o para remover diret√≥rio recursivamente
function removeDirectory(dirPath) {
  if (dirExists(dirPath)) {
    try {
      fs.rmSync(dirPath, { recursive: true, force: true });
      return true;
    } catch (error) {
      log(`‚ö†Ô∏è  Erro ao remover ${dirPath}: ${error.message}`, 'yellow');
      return false;
    }
  }
  return true;
}

// Fun√ß√£o para remover arquivo
function removeFile(filePath) {
  if (fileExists(filePath)) {
    try {
      fs.unlinkSync(filePath);
      return true;
    } catch (error) {
      log(`‚ö†Ô∏è  Erro ao remover ${filePath}: ${error.message}`, 'yellow');
      return false;
    }
  }
  return true;
}

// Fun√ß√£o para executar comandos com tratamento de erro
function runCommand(command, description, optional = false) {
  logStep('CMD', description, 'blue');
  try {
    execSync(command, { stdio: 'inherit', cwd: process.cwd() });
    logStep('‚úì', `${description} - Conclu√≠do`, 'green');
    return true;
  } catch (error) {
    if (optional) {
      logStep('‚ö†Ô∏è', `${description} - Falhou (opcional)`, 'yellow');
      return false;
    } else {
      logStep('‚úó', `${description} - Falhou`, 'red');
      throw error;
    }
  }
}

// Fun√ß√£o para obter tamanho de diret√≥rio
function getDirSize(dirPath) {
  if (!dirExists(dirPath)) return 0;
  
  let totalSize = 0;
  try {
    const files = fs.readdirSync(dirPath, { withFileTypes: true });
    
    for (const file of files) {
      const filePath = path.join(dirPath, file.name);
      if (file.isDirectory()) {
        totalSize += getDirSize(filePath);
      } else {
        try {
          totalSize += fs.statSync(filePath).size;
        } catch (error) {
          // Arquivo pode ter sido removido durante a leitura
        }
      }
    }
  } catch (error) {
    // Diret√≥rio pode n√£o existir mais
  }
  
  return totalSize;
}

// Fun√ß√£o para formatar tamanho em bytes
function formatBytes(bytes) {
  if (bytes === 0) return '0 Bytes';
  const k = 1024;
  const sizes = ['Bytes', 'KB', 'MB', 'GB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
}

